package day11_Switch_Scanner;

public class Grade2 {
    public static void main(String[] args) {
        char ch='A';
        String result="";
        switch (ch){
            case 'A':
                result="Excellent";
            case 'B':
                result="good";
            case 'C':
                result="Midle";
            case 'D':
                result="X";
            default:
                result="Failed";
                System.out.println(result);









        }
    }
}
