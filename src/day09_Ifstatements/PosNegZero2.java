package day09_Ifstatements;

public class PosNegZero2 {
    public static void main(String[] args) {
        int n =1;
        boolean positive=n>0,
                negative=n<0;
        if (positive){
            System.out.println("n=positive");
        }else if (negative){
            System.out.println("n=negative");
        }else {
            System.out.println("zero");
        }





    }
}
