package day19_Recap;

public class LoopsWithoutCurlyBraces {
    public static void main(String[] args) {
        for (int i =0;i<5;i++){
            System.out.println("ismail");
            System.out.println("Atici");
        }


        for (int i =0;i<5;i++)
            System.out.println("ismail");
        for (int i =0;i<5;i++)
            System.out.println("Atici");


    }
}
